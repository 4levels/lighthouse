<?php

namespace Nuwave\Lighthouse\Support\Interfaces;

interface RelayType
{
    /**
     * Get model by id.
     *
     * Note: When the root 'node' query is called, this method
     * will be used to resolve the type by providing the id.
     *
     * @param  mixed $id
     * @return mixed
     */
    public function resolveById($id);
}
